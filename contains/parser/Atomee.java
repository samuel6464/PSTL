package parser;

public class Atomee {
	private int zoom;
	private boolean breakline;
	private int atomeId;
	private String type;
	private String nom;
	private int alteration;
	private double commencement;
	private double fin;
	private int positionDebut;
	private int positionFin;
	public int getZoom() {
		return zoom;
	}
	public void setZoom(int zoom) {
		this.zoom = zoom;
	}
	public boolean isBreakline() {
		return breakline;
	}
	public void setBreakline(boolean breakline) {
		this.breakline = breakline;
	}
	public int getAtomeId() {
		return atomeId;
	}
	public void setAtomeId(int atomeId) {
		this.atomeId = atomeId;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public int getAlteration() {
		return alteration;
	}
	public void setAlteration(int alteration) {
		this.alteration = alteration;
	}
	public double getCommencement() {
		return commencement;
	}
	public void setCommencement(double commencement) {
		this.commencement = commencement;
	}
	public double getFin() {
		return fin;
	}
	public void setFin(double fin) {
		this.fin = fin;
	}
	public int getPositionDebut() {
		return positionDebut;
	}
	public void setPositionDebut(int positionDebut) {
		this.positionDebut = positionDebut;
	}
	public int getPositionFin() {
		return positionFin;
	}
	public void setPositionFin(int positionFin) {
		this.positionFin = positionFin;
	}
	
	
}
